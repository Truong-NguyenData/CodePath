# Floor Division
# The // operator is also known as floor division. x // y
# returns the result of x divided by y rounded down to the nearest whole integer.
# Example Usage:

print(5 // 2) # Prints 2 because 5 / 2 = 2.5 which rounded down is 2
print(10 // 2) # Prints 5 because 10 // 2 = 5 which is already an integer

#So how about 5/2
print(5 / 2)